#pragma once

#include <stdint.h>

/* General request-reply port interface */
template <typename T, typename R> class AtomicPort {
  public:
    typedef T req_type;
    typedef R resp_type;
    // Returns response cycle
    // FIXME(mcj) a very cache-centric name.
    virtual uint64_t access(T& req, R& resp, uint64_t cycle) = 0;
};


