#include "swarm/hooks.h"
#include <assert.h>

int main(int argc, char* argv[]) {
    assert(sim_in_ff());
    zsim_roi_begin();
    // N.B. This assertion will fail for configs that set ffHeartbeats so that
    // we fast-forward through the whole ROI.
    assert(!sim_in_ff());
    zsim_roi_end();
    assert(sim_in_ff());

    return 0;
}

